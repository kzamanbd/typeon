export const generateRandomText = (language: 'english' | 'bengali', wordCount: number = 50): string => {
    const words = randomWords[language];
    const selectedWords: string[] = [];

    for (let i = 0; i < wordCount; i++) {
        const randomIndex = Math.floor(Math.random() * words.length);
        selectedWords.push(words[randomIndex]);
    }

    return selectedWords.join(' ');
};

export const allPracticeTexts = [...englishPracticeTexts, ...bengaliPracticeTexts];

export const getPracticeTextsByLanguage = (language: 'english' | 'bengali') => {
    return allPracticeTexts.filter(text => text.language === language);
};

export const getPracticeTextById = (id: string) => {
    return allPracticeTexts.find(text => text.id === id);
};
